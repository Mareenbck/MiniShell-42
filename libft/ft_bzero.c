#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*s2;

	s2 = s;
	i = 0;
	while (n > 0)
	{
		s2[i] = '\0';
		n--;
		i++;
	}
}
